public class ConditionalStatements {
    public static void main(String[] args) {
        int number1=65;
        int number2=88;
        if (number2>number1) {
            System.out.println(number2+" is greater than "+number1);
        }
        else{
            System.out.println(number1+" is greater than "+number2);
        }
    }
}
