public class condition {
    public static void main(String[] args) {
        int number=3;
        switch(number){
            case 1:
                System.out.println("on-duty");
                break;
            case 2:
                System.out.println("Absent");
                break;
            case 3:
                System.out.println("Present");
                break;
            default:
                System.out.println("Default");
        }
    }
}
